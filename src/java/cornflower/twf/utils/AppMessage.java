package cornflower.twf.utils;

/**
 *
 * @author J-Mo
 */
public class AppMessage {
    
    private String type;
    private String message;
    
    public AppMessage(String type, String message) {
        this.type = type;
        this.message = message;
    }

    public String getType() {
        return type;
    }

    public String getMessage() {
        return message;
    }
}
